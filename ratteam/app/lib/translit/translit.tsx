export const translit = (text: string) => {
  const clearedText = text.replace(/[ьъЬЪ\s]|[^a-zA-Zа-яА-ЯёЁ]/g, '');

  const alphabet: Record<string, string> = {
    а: 'a',
    б: 'b',
    в: 'v',
    г: 'g',
    д: 'd',
    е: 'e',
    ё: 'e',
    ж: 'zh',
    з: 'z',
    и: 'i',
    й: 'y',
    к: 'k',
    л: 'l',
    м: 'm',
    н: 'n',
    о: 'o',
    п: 'p',
    р: 'r',
    с: 's',
    т: 't',
    у: 'u',
    ф: 'f',
    х: 'kh',
    ц: 'ts',
    ч: 'ch',
    ш: 'sh',
    щ: 'shch',
    ы: 'y',
    э: 'e',
    ю: 'yu',
    я: 'ya',
  };

  return clearedText
    .split('')
    .map((char) => alphabet[char.toLowerCase()] || char)
    .join('');
};
